package com.shape;


public class Point
{
    private int x;
    private int y;
    public void setX( int xValue )
    {
        x = xValue;
    }
    public int getX(  )    {
        return this.x;
    }

    public void setY( int yValue )    {
        y = yValue;
    }


    public int getY(  )    {
        return this.y;
    }
}
