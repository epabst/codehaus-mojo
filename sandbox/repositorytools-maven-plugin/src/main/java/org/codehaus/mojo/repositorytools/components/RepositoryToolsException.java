package org.codehaus.mojo.repositorytools.components;

public class RepositoryToolsException extends Exception
{

	private static final long serialVersionUID = 3083937184482648483L;

	public RepositoryToolsException()
	{
		super();
	}

	public RepositoryToolsException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public RepositoryToolsException(String message)
	{
		super(message);
	}

	public RepositoryToolsException(Throwable cause)
	{
		super(cause);
	}

}
